const mongoose = require("mongoose");

module.exports = new mongoose.Schema(
  {
    firstName: {
      type: String,
      required: true,
    },
    lastName: {
      type: String,
      required: true,
    },
    email: {
      type: String,
      required: true,
      unique: true,
    },
    password: {
      type: String,
      required: true,
    },
    avatar: String,
    phone: String,
    dob: Date
  },
  // additional configuration
  {
    timestamps: true
  }
);
